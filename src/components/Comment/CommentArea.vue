<script >
import {ref} from 'vue'
import { getCommentListService, addCommentService } from '@/api/comment.js'
import useUserInfoStore from '@/stores/userInfo.js'
// const userInfoStore = useUserInfoStore();
var commentList = ref([])
const clickoutside = {
    // 初始化指令
    bind(el, binding, vnode) {
        function documentHandler(e) {
            // 这里判断点击的元素是否是本身，是本身，则返回
            if (el.contains(e.target)) {
                return false;
            }
            // 判断指令中是否绑定了函数
            if (binding.expression) {
                // 如果绑定了函数 则调用那个函数，此处binding.value就是handleClose方法
                binding.value(e);
            }
        }
        // 给当前元素绑定个私有变量，方便在unbind中可以解除事件监听
        el.vueClickOutside = documentHandler;
        document.addEventListener('click', documentHandler);
    },
    update() { },
    unbind(el, binding) {
        // 解除事件监听
        document.removeEventListener('click', el.vueClickOutside);
        delete el.vueClickOutside;
    },
};
const getCommentList = async () => {
    const result = await getCommentListService()
    commentList.value = result.data
    //console.log(userInfoStore.info.name)
}
getCommentList()
export default {
    name: 'ArticleComment',
    data() {
        return {
            btnShow: false,
            index: '0',
            replyComment: '',
            myName: '',
            myHeader: 'https://red-travel-system.oss-cn-wuhan-lr.aliyuncs.com/b9876cc1-2710-43bd-a19e-7907b20b52b4.jpeg',
            myId: 1,
            to: '',
            toCommentId: 0,
            comments: commentList
        }
    },

    directives: { clickoutside },
    methods: {
        inputFocus() {
            var replyInput = document.getElementById('replyInput');
            replyInput.style.padding = "8px 8px"
            replyInput.style.border = "2px solid blue"
            replyInput.focus()
        },
        showReplyBtn() {
            this.btnShow = true
        },
        hideReplyBtn() {
            this.btnShow = false
            replyInput.style.padding = "10px"
            replyInput.style.border = "none"
        },
        showReplyInput(i, name, id) {
            this.comments[this.index].inputShow = false
            this.index = i
            this.comments[i].inputShow = true
            this.tousername = name
            this.tocommentId = id
        },
        _inputShow(i) {
            return this.comments[i].inputShow
        },
        sendComment() {
            if (!this.replyComment) {
                this.$message({
                    showClose: true,
                    type: 'warning',
                    message: '评论不能为空'
                })
            } else {
                let a = {}
                let input = document.getElementById('replyInput')
                let timeNow = new Date().getTime();
                let commitTime = this.dateStr(timeNow);
                a.name = this.myName
                a.content = this.replyComment
                a.image = this.myHeader
                a.commitTime = commitTime
                this.comments.push(a)
                this.replyComment = ''
                input.innerHTML = '';
                let b = {}
                b.userId = this.myId
                b.content = a.content
                b.toCommentId = 0
                b.toUserName = '0'
                console.log(b)
                addCommentService(b)
            }
        },
        sendCommentReply(i, j) {
            if (!this.replyComment) {
                this.$message({
                    showClose: true,
                    type: 'warning',
                    message: '评论不能为空'
                })
            } else {
                let a = {}
                let timeNow = new Date().getTime();
                let commitTime = this.dateStr(timeNow);
                a.name = this.myName
                a.toUserName = this.tousername
                a.image = this.myHeader
                a.content = this.replyComment
                a.commitTime = commitTime
                this.comments[i].reply.push(a)
                this.replyComment = ''
                document.getElementsByClassName("reply-comment-input")[i].innerHTML = ""
                let b = {}
                b.userId = this.myId
                b.content = a.content
                b.toCommentId = this.tocommentId
                b.toUserName = this.tousername
                console.log(b)
                addCommentService(b)
            }
        },
        onDivInput: function (e) {
            this.replyComment = e.target.innerHTML;
        },
        dateStr(date) {
            //获取js 时间戳
            var time = new Date().getTime();
            //去掉 js 时间戳后三位，与php 时间戳保持一致
            time = parseInt((time - date) / 1000);
            //存储转换值 
            var s;
            if (time < 60 * 10) {//十分钟内
                return '刚刚';
            } else if ((time < 60 * 60) && (time >= 60 * 10)) {
                //超过十分钟少于1小时
                s = Math.floor(time / 60);
                return s + "分钟前";
            } else if ((time < 60 * 60 * 24) && (time >= 60 * 60)) {
                //超过1小时少于24小时
                s = Math.floor(time / 60 / 60);
                return s + "小时前";
            } else if ((time < 60 * 60 * 24 * 30) && (time >= 60 * 60 * 24)) {
                //超过1天少于30天内
                s = Math.floor(time / 60 / 60 / 24);
                return s + "天前";
            } else {
                //超过30天ddd
                var date = new Date(parseInt(date));
                return date.getFullYear() + "/" + (date.getMonth() + 1) + "/" + date.getDate();
            }
        }
    },
    created() {
        const userInfoStore = useUserInfoStore();
        this.myName = userInfoStore.info.name
        this.myHeader = userInfoStore.info.image
        this.myId = userInfoStore.info.id
    }
}
</script>
